    public static string Lang(HttpContext context) =>
        string.Equals(context.Request.Cookies["skillbridge_lang"], "en", StringComparison.OrdinalIgnoreCase)
            ? "en"
            : "vi";

    public static string T(HttpContext context, string key) => T(Lang(context), key);

    public static string T(string lang, string key)
    {
        if (lang != "en" && ViOverrides.TryGetValue(key, out var normalized))
        {
            return normalized;
        }

        var source = lang == "en" ? En : Vi;
        return source.TryGetValue(key, out var value) ? value : key;
    }
